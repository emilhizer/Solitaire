  class func pantoneOrangePopsicle(alpha: CGFloat = 1.0) -> UIColor { return UIColor(red: 255/255.0, green: 121/255.0,  blue: 19/255.0, alpha: alpha) }
  class func pantoneFandango(alpha: CGFloat = 1.0) -> UIColor { return UIColor(red: 224/255.0, green: 79/255.0,  blue: 128/255.0, alpha: alpha) }
  class func pantoneDrizzle(alpha: CGFloat = 1.0) -> UIColor { return UIColor(red: 160/255.0, green: 159/255.0,  blue: 156/255.0, alpha: alpha) }
  class func pantonePrismPink(alpha: CGFloat = 1.0) -> UIColor { return UIColor(red: 240/255.0, green: 161/255.0,  blue: 191/255.0, alpha: alpha) }
  class func pantoneGreenery(alpha: CGFloat = 1.0) -> UIColor { return UIColor(red: 136/255.0, green: 176/255.0,  blue: 75/255.0, alpha: alpha) }

}
